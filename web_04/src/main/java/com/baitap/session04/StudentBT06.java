package com.baitap.session04;

public class StudentBT06 {
    int id;
    String name;
    int age;
    double gpa;

    public StudentBT06(int id, String name, int age, double gpa) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.gpa = gpa;
    }

    public int getId() { return id; }
    public String getName() { return name; }
    public int getAge() { return age; }
    public double getGpa() { return gpa; }
}
